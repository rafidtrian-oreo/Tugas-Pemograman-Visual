    private void hitungGrandTotal() {
    int totalBelanja = 0;
    int jumlahBaris = tbltransaksi.getRowCount();
    
    for (int i = 0; i < jumlahBaris; i++) {
        // FIX: Ubah dari angka 4 menjadi 5 agar mengambil kolom "Total", bukan "QTY"
        int totalBarang = Integer.parseInt(tbltransaksi.getValueAt(i, 5).toString());
        totalBelanja += totalBarang;
    }
    
    // Set ke textfield kanan bawah milikmu (sesuaikan namanya jika bukan txtgrandtotal)
    txtgrandtotal.setText(String.valueOf(totalBelanja));
}
    
