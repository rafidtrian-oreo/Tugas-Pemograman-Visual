    private void btambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btambahActionPerformed
    try {
        // Validasi agar tidak bisa tambah kalau total barang masih kosong/0
        if(txttotal.getText().isEmpty() || txttotal.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "QTY dan Total Barang belum diisi!");
            return;
        }

        // Ambil model dari tbltransaksi
        DefaultTableModel model = (DefaultTableModel) tbltransaksi.getModel();
        
        // Masukkan data sesuai urutan kolom tabel transaksimu
        model.addRow(new Object[]{
            txtkdbrg.getText(),
            txtnmbrg.getText(),
            txthb.getText(),
            txthj.getText(),
            txtqty.getText(),
            txttotal.getText()
        });
        
        
        hitungGrandTotal();
        
        // Bersihkan kolom input barang saja (jangan panggil kosong() yang menghapus nomor nota)
        txtkdbrg.setText("");
        txtnmbrg.setText("");
        txthb.setText("");
        txthj.setText("");
        txtqty.setText("");
        txttotal.setText("");
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menambahkan ke tabel: " + e.getMessage());
    }
    }//GEN-LAST:event_btambahActionPerformed

